package ch_02_ex01;

public class LiteralExample {
	public static void main(String[] args) {
	// Type 변수명  변수값
		int var1 = 10;
		
		System.out.println("var1 -> "+var1);
		
		// 숫자 앞에 '0'을 붙이명 eclipse 에서 8진수로 인식
		int var2 = 010;
		System.out.println("var2 -> "+var2);
		
		int var3 = 0x10;
		System.out.println("var3 -> "+var3);
		
	}

}
